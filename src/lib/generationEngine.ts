function generateTaskBreakdown(bp: Blueprint): string {
  const name = bp.basics.name || 'Project';
  const lines: string[] = [];
  lines.push(`# Task Breakdown — ${name}`);
  lines.push('');

  let phase = 1;

  // Phase 1: Setup
  lines.push(`## Phase ${phase}: Project Setup`);
  lines.push('');
  lines.push('- [ ] Initialize project repository');
  const tech = techByCategory(bp);
  for (const [cat, names] of tech) {
    lines.push(`- [ ] Set up ${cat}: ${names.join(', ')}`);
  }
  lines.push('- [ ] Configure development environment');
  lines.push('- [ ] Set up CI/CD pipeline');
  lines.push('');
  phase++;

  // Phase 2: Core features
  if (bp.features.selected.length > 0) {
    lines.push(`## Phase ${phase}: Core Features`);
    lines.push('');
    for (const id of bp.features.selected) {
      const opt = featureOptions.find((o) => o.id === id);
      lines.push(`### ${lookupName(id)}`);
      lines.push(`- [ ] Design ${lookupName(id).toLowerCase()} UI/UX`);
      lines.push(`- [ ] Implement backend logic`);
      lines.push(`- [ ] Build frontend components`);
      lines.push(`- [ ] Write tests`);
      if (opt) {
        lines.push(`- [ ] Estimated difficulty: ${opt.difficulty}`);
      }
      lines.push('');
    }
    phase++;
  }

  // Phase 3: Integrations
  if (bp.integrations.selected.length > 0) {
    lines.push(`## Phase ${phase}: Integrations`);
    lines.push('');
    for (const id of bp.integrations.selected) {
      lines.push(`- [ ] Integrate ${lookupName(id)}`);
      lines.push(`- [ ] Test ${lookupName(id)} integration`);
    }
    lines.push('');
    phase++;
  }

  // Phase 4: Quality
  const qual = qualityByCategory(bp);
  if (qual.length > 0) {
    lines.push(`## Phase ${phase}: Quality & Non-Functional`);
    lines.push('');
    for (const [cat, names] of qual) {
      lines.push(`### ${cat}`);
      for (const n of names) lines.push(`- [ ] Implement ${n}`);
      lines.push('');
    }
    phase++;
  }

  // Final phase
  lines.push(`## Phase ${phase}: Launch`);
  lines.push('');
  lines.push('- [ ] Final QA testing');
  lines.push('- [ ] Performance optimization');
  lines.push('- [ ] Security audit');
  lines.push('- [ ] Deploy to production');
  lines.push('- [ ] Monitor and iterate');
  lines.push('');

  lines.push('---');
  lines.push(`*Generated by Blueprint Builder on ${today()}*`);
  return lines.join('\n');
}

