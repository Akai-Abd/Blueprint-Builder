function generatePRD(bp: Blueprint): string {
  const lines: string[] = [];
  const name = bp.basics.name || 'Untitled Project';
  lines.push(`# Product Requirements Document (PRD)`);
  lines.push(`## ${name}`);
  lines.push('');
  lines.push(`**Date:** ${today()}`);
  lines.push(`**Status:** ${bp.status === 'complete' ? 'Final' : 'Draft'}`);
  lines.push('');

  // Overview
  lines.push('## 1. Overview');
  lines.push('');
  lines.push(`**Project Name:** ${name}`);
  lines.push(`**Project Type:** ${fmt(bp.basics.projectType)}`);
  lines.push(`**Industry:** ${bp.basics.industry || 'Not specified'}`);
  lines.push(`**Target Audience:** ${bp.basics.targetAudience || 'Not specified'}`);
  lines.push(`**Business Model:** ${fmt(bp.basics.businessModel)}`);
  lines.push('');
  if (bp.basics.description) {
    lines.push('### Description');
    lines.push('');
    lines.push(bp.basics.description);
    lines.push('');
  }

  // Platforms
  lines.push('## 2. Target Platforms');
  lines.push('');
  if (bp.basics.targetPlatforms.length > 0) {
    for (const p of bp.basics.targetPlatforms) {
      lines.push(`- ${fmt(p)}`);
    }
  } else {
    lines.push('_No platforms selected._');
  }
  lines.push('');

  // Technology Stack
  lines.push('## 3. Technology Stack');
  lines.push('');
  const tech = techByCategory(bp);
  if (tech.length > 0) {
    lines.push('| Layer | Technologies |');
    lines.push('|-------|-------------|');
    for (const [cat, names] of tech) {
      lines.push(`| **${cat}** | ${names.join(', ')} |`);
    }
  } else {
    lines.push('_No technologies selected._');
  }
  lines.push('');

  // Features
  lines.push('## 4. Features');
  lines.push('');
  if (bp.features.selected.length > 0) {
    for (const id of bp.features.selected) {
      const opt = featureOptions.find((o) => o.id === id);
      lines.push(`### ${opt?.icon ?? '•'} ${lookupName(id)}`);
      if (opt?.description) lines.push(`${opt.description}`);
      lines.push('');
    }
  } else {
    lines.push('_No features selected._');
    lines.push('');
  }

  // Integrations
  lines.push('## 5. Integrations');
  lines.push('');
  if (bp.integrations.selected.length > 0) {
    for (const id of bp.integrations.selected) {
      const opt = integrationOptions.find((o) => o.id === id);
      lines.push(`- **${lookupName(id)}** — ${opt?.description ?? ''}`);
    }
  } else {
    lines.push('_No integrations selected._');
  }
  lines.push('');

  // Quality
  lines.push('## 6. Quality & Non-Functional Requirements');
  lines.push('');
  const qual = qualityByCategory(bp);
  if (qual.length > 0) {
    for (const [cat, names] of qual) {
      lines.push(`### ${cat}`);
      for (const n of names) lines.push(`- ${n}`);
      lines.push('');
    }
  } else {
    lines.push('_No quality configurations set._');
    lines.push('');
  }

  lines.push('---');
  lines.push(`*Generated by Blueprint Builder on ${today()}*`);
  return lines.join('\n');
}

