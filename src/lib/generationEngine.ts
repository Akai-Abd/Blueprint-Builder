export function generateDocuments(
  blueprint: Blueprint,
  selectedIds: string[]
): GeneratedDocument[] {
  return selectedIds
    .map((id) => documentTypes.find((d) => d.id === id))
    .filter((d): d is DocumentType => d !== undefined)
    .map((doc) => ({
      id: doc.id,
      title: doc.title,
      icon: doc.icon,
      description: doc.description,
      content: doc.generate(blueprint),
    }));
}
