function generateDatabaseDesign(bp: Blueprint): string {
  const name = bp.basics.name || 'Project';
  const lines: string[] = [];
  lines.push(`# Database Design — ${name}`);
  lines.push('');

  const dbs = bp.technology.database.map(lookupName);
  lines.push(`**Database:** ${dbs.length > 0 ? dbs.join(', ') : 'Not selected'}`);
  const orms = bp.technology.orm.map(lookupName);
  if (orms.length > 0) lines.push(`**ORM:** ${orms.join(', ')}`);
  lines.push('');

  // Core tables
  lines.push('## Core Tables');
  lines.push('');

  if (bp.features.selected.includes('authentication')) {
    lines.push('### users');
    lines.push('```sql');
    lines.push('CREATE TABLE users (');
    lines.push('  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),');
    lines.push('  email       VARCHAR(255) UNIQUE NOT NULL,');
    lines.push('  name        VARCHAR(255) NOT NULL,');
    lines.push('  password    VARCHAR(255) NOT NULL,');
    lines.push('  avatar_url  TEXT,');
    lines.push('  role        VARCHAR(50) DEFAULT \'user\',');
    lines.push('  created_at  TIMESTAMPTZ DEFAULT NOW(),');
    lines.push('  updated_at  TIMESTAMPTZ DEFAULT NOW()');
    lines.push(');');
    lines.push('```');
    lines.push('');
  }

  for (const id of bp.features.selected) {
    if (id === 'authentication') continue;
    const tableName = id.replace(/-/g, '_');
    lines.push(`### ${tableName}`);
    lines.push('```sql');
    
    if (id === 'dashboard') {
      lines.push(`CREATE TABLE dashboard_metrics (`);
      lines.push('  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),');
      lines.push('  metric_name VARCHAR(255) NOT NULL,');
      lines.push('  value       NUMERIC NOT NULL,');
      lines.push('  recorded_at TIMESTAMPTZ DEFAULT NOW()');
      lines.push(');');
    } else if (id === 'user-profiles') {
      lines.push(`CREATE TABLE user_profiles (`);
      lines.push('  user_id     UUID PRIMARY KEY REFERENCES users(id),');
      lines.push('  bio         TEXT,');
      lines.push('  website     VARCHAR(255),');
      lines.push('  preferences JSONB DEFAULT \'{}\',');
      lines.push('  updated_at  TIMESTAMPTZ DEFAULT NOW()');
      lines.push(');');
    } else if (id === 'payments') {
      lines.push(`CREATE TABLE subscriptions (`);
      lines.push('  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),');
      if (bp.features.selected.includes('authentication')) {
        lines.push('  user_id     UUID REFERENCES users(id),');
      }
      lines.push('  plan_id     VARCHAR(100) NOT NULL,');
      lines.push('  status      VARCHAR(50) NOT NULL,');
      lines.push('  current_period_end TIMESTAMPTZ,');
      lines.push('  created_at  TIMESTAMPTZ DEFAULT NOW()');
      lines.push(');');
    } else if (id === 'messaging') {
      lines.push(`CREATE TABLE messages (`);
      lines.push('  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),');
      lines.push('  conversation_id UUID NOT NULL,');
      if (bp.features.selected.includes('authentication')) {
        lines.push('  sender_id   UUID REFERENCES users(id),');
      }
      lines.push('  content     TEXT NOT NULL,');
      lines.push('  created_at  TIMESTAMPTZ DEFAULT NOW()');
      lines.push(');');
    } else {
      lines.push(`CREATE TABLE ${tableName} (`);
      lines.push('  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),');
      if (bp.features.selected.includes('authentication')) {
        lines.push('  user_id     UUID REFERENCES users(id),');
      }
      lines.push('  title       VARCHAR(255) NOT NULL,');
      lines.push('  data        JSONB DEFAULT \'{}\',');
      lines.push('  status      VARCHAR(50) DEFAULT \'active\',');
      lines.push('  created_at  TIMESTAMPTZ DEFAULT NOW(),');
      lines.push('  updated_at  TIMESTAMPTZ DEFAULT NOW()');
      lines.push(');');
    }
    lines.push('```');
    lines.push('');
  }

  lines.push('---');
  lines.push(`*Generated by Blueprint Builder on ${today()}*`);
  return lines.join('\n');
}

function generateUserStories(bp: Blueprint): string {
  const name = bp.basics.name || 'Project';
  const lines: string[] = [];
  lines.push(`# User Stories — ${name}`);
  lines.push('');

  let storyNum = 1;
  for (const id of bp.features.selected) {
    const opt = featureOptions.find((o) => o.id === id);
    const featureName = lookupName(id);
    lines.push(`## US-${String(storyNum).padStart(3, '0')}: ${featureName}`);
    lines.push('');
    lines.push(`**As a** user`);
    lines.push(`**I want to** use the ${featureName.toLowerCase()} feature`);
    
    // Fix grammar by using description instead of raw bestUseCase
    let reason = opt?.description ? opt.description.toLowerCase() : `benefit from ${featureName.toLowerCase()}`;
    if (reason.endsWith('.')) reason = reason.slice(0, -1);
    lines.push(`**So that** I can achieve: ${reason}`);
    lines.push('');
    lines.push('### Acceptance Criteria');
    lines.push('');
    if (opt) {
      for (const adv of opt.advantages.slice(0, 3)) {
        lines.push(`- [ ] ${adv} is implemented and working`);
      }
    }
    lines.push(`- [ ] ${featureName} is accessible and responsive`);
    lines.push(`- [ ] Error states are handled gracefully`);
    lines.push(`- [ ] Unit tests cover core functionality`);
    lines.push('');
    lines.push(`**Priority:** ${opt && opt.popularity > 80 ? 'High' : opt && opt.popularity > 50 ? 'Medium' : 'Low'}`);
    lines.push(`**Difficulty:** ${opt?.difficulty ?? 'intermediate'}`);
    lines.push('');
    storyNum++;
  }

  if (bp.features.selected.length === 0) {
    lines.push('_No features selected. Add features to generate user stories._');
    lines.push('');
  }

  lines.push('---');
  lines.push(`*Generated by Blueprint Builder on ${today()}*`);
  return lines.join('\n');
}

