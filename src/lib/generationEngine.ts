  lines.push('');

  lines.push('## Environment Variables');
  lines.push('');
  lines.push('```env');
  lines.push('NODE_ENV=production');
  lines.push('DATABASE_URL=your_database_url');
  if (bp.features.selected.includes('authentication')) {
    lines.push('JWT_SECRET=your_jwt_secret');
    lines.push('SESSION_SECRET=your_session_secret');
  }
  for (const id of bp.integrations.selected) {
    const key = id.toUpperCase().replace(/-/g, '_');
    lines.push(`${key}_API_KEY=your_${id}_api_key`);
  }
  lines.push('```');
